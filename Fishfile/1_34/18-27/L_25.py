# 创建和访问字典，标志性大括号 ： { key:words}    
emptydict = {}

dict1 = {'老四':'大家好，我是老四',\
         '阿雄':'一条这么小的石九公啊!',\
         '阿峰':'这是要发财的节奏！',\
         '华农兄弟':'这只竹鼠中暑了，这样下去不行，不如......',\
         '信誓蛋蛋':'Life is fantastic'}
print('王下七赶海阿峰说:',dict1['阿峰'])

dict2 = {1:'老四',2:'阿雄',3:'华农兄弟',4:'信誓蛋蛋'}
